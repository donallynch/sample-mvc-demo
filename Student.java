public class Student {

    private String rollNo;
    private String name;

    /**
     * @return
     */
    public String getRollNo() {
        return rollNo;
    }

    /**
     * @param rollNo
     */
    public void setRollNo(String rollNo) {
        this.rollNo = rollNo;
    }

    /**
     * @return
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }
}

